from math import *   # if you want to call all the functions from math module

'''
# Importing specific functions from the math module for better code readability
from math import max, min, abs, pow, sqrt, round, floor, ceil
'''

# Find the maximum of two numbers (returns 10)
print(max(10, 5))  # Output: 10

# Find the minimum of two numbers (returns 5)
print(min(10, 5))  # Output: 5

# Calculate the absolute value of a number (returns 4)
print(abs(-4))  # Output: 4

# Calculate the power of a number (2 to the power of 3, returns 8)
print(pow(2, 3))  # Output: 8

# Calculate the square root of a number (returns approximately 2.236)
print(sqrt(5))  # Output: approximately 2.236

# Round a floating-point number to the nearest integer (returns 5)
print(round(5.454))  # Output: 5

# Find the floor value of a floating-point number (returns 7)
print(floor(7.7784))  # Output: 7

# Find the ceiling value of a floating-point number (returns 8)
print(ceil(7.678))  # Output: 8
