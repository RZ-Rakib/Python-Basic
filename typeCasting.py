# Changing the date type is called Type-casting.

# method 1
num1 = input("what is number 1? ") # input is a string
num2 = input("what is number 2? ") # input is a string

sum = int(num1) + int(num2) # type-casting
print("The sum is",sum)


# method 2
num1 = int(input("what is number 1? ")) # input is a string
num2 = int(input("what is number 2? ")) # input is a string

sum = num1 + num2 # type-casting
print("The sum is",sum)