# range function genarate numbers based on the range

number = list(range(15)) # genarate and convert it to a list
print(number)
print(number[4]) # check the 5th number by using the index number

number2 = list(range(5, 15)) # start, end
print(number2)

number3 = list(range(10, 100, 5)) # start, end, interval
print(number3)